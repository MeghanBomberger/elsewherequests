export const selectRandomNames = () => {
  const randomNames = []

  while (randomNames.length < 5) {
    const index = Math.floor(Math.random() * names.length)
    randomNames.filter(name => name === names[index]).length === 0 && randomNames.push(names[index])
  }

  return randomNames
}